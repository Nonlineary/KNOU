## 01 : R의 base 패키지 중 하나인 datasets에는 많은 기본 데이터가 제공된다. 그중에서 data(faithful) 데이터를 이용하여 다음을 수행하라.

### 1) 도움말 (help faithful)을 이용하여 데이터의 내용, 변수명, 소스 등을 확인하라
help(faithful)

### 2) dim() 함수를 이용하여 데이터세트의 크기를 확인하라
dim(faithful)

### 3) summary() 함수를 이용하여 변수에 대한 기술통계량을 확인하라.
summary(faithful)

### 4) plot() 명령문을 이용하여 faithful 데이터 내 두 변수에 대한 상관관계를 기술하라.
